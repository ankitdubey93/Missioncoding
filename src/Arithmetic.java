import java.awt.desktop.SystemEventListener;

public class Arithmetic {
    public static void main(String[] args){
        int a;
        int b;

        a = 10;
        b = 4;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a % b));


    }
}
